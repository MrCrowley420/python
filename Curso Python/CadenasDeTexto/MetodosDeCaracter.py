cadena = 'Estoy utilIzando lOs metodos de python'

#metodos . luego el nombre del metodo
print(cadena.lower())  #transforma todas las mayusculas en minusculas

print(cadena.upper())  #trasnforma todas las minusculas en mayusculas

print(cadena.capitalize()) #muestra solo la primer letra en mayuscula

print(cadena.title())  #muestra la primer letra de cada palabra del string en mayuscula

print(cadena.swapcase())  #invierte las mayusculas y las minusculas